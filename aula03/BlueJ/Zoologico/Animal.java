public class Animal
{
    // variáveis de instância - substitua o exemplo abaixo pelo seu próprio
    private String especie;
    private String raca;
    private String nome;
    private boolean ehDomestico;

    /**
     * Construtor para objetos da classe Animal
     */
    public Animal(String especie, String raca, String nome, boolean ehDomestico)
    {
        // inicializa variáveis de instância
        this.especie = especie;
        this.raca = raca;
        this.nome = nome;
        this.ehDomestico = ehDomestico;
    }
}
